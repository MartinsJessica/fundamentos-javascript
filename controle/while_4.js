let controle = 1; //qtd determinada de repetição, criando uma variavel que vai controlar meu laço
while (controle <= 10) { //expressão verdadeira que precisaria mdar para false
    console.log(controle);
    controle++; // ele vai mostrar 1 depois incrementa por exemplo chegou em 11 <= 10 false, então ai ele vai sair do laço do bloco while

}

console.log("Fim!")

